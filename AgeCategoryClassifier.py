
age = int(input("Veuillez entrer l'âge de l'enfant : "))


if age >= 6 and age <= 7:
    print("Sa catégorie est poussin.")
elif age >= 8 and age <= 9:
    print("Sa catégorie est pupille.")
elif age >= 10 and age <= 11:
    print("Sa catégorie est minime.")
elif age == 12:
    print("Sa catégorie est cadet.")
